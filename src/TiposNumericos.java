public class TiposNumericos {
    public static void main(String[] args) {
        Byte numeroByte = 127; // -128 até 127

        Short numericoShort = -32768; // -32768 até 32767

        Integer numeroInteger = 2147483647; // -2147483648 até 2147483647

        Long numeroLong = 1l; // -9,22337237*10^18

        Float numeroFloat = 1.0f;

        Double numeroDouble = 1.0;
    }
}
